#ifndef _zf_device_pd1503_h_
#define _zf_device_pd1503_h_

#include "zf_common_typedef.h"

//=================================================PD1503 参数配置区================================================
#define PD1503_USE_SOFT_SPI                 (0)                                 // 默认使用硬件 SPI 方式驱动
#if PD1503_USE_SOFT_SPI
//====================================================软件 SPI 引脚====================================================
#define PD1503_SOFT_SPI_DELAY               (1)                                 // 软件 SPI 延时周期 数值越小 SPI 通信速率越快
#define PD1503_SCLK_PIN                     (P22_3)                             // 软件 SPI SCK 引脚
#define PD1503_MOSI_PIN                     (P22_0)                             // 软件 SPI MOSI 引脚
#define PD1503_MISO_PIN                     (P22_1)                             // 软件 SPI MISO 引脚
//====================================================软件 SPI 引脚====================================================
#else
//====================================================硬件 SPI 引脚====================================================
#define PD1503_SPI_SPEED                    (6 * 1000 * 1000)                   // 硬件 SPI 速率 最大10MHz
#define PD1503_SPI                          (SPI_4)                             // 硬件 SPI 号
#define PD1503_SCLK_PIN                     (SPI4_SCLK_P22_3)                   // 硬件 SPI SCK 引脚
#define PD1503_MOSI_PIN                     (SPI4_MOSI_P22_0)                   // 硬件 SPI MOSI 引脚
#define PD1503_MISO_PIN                     (SPI4_MISO_P22_1)                   // 硬件 SPI MISO 引脚
//====================================================硬件 SPI 引脚====================================================
#endif

#define PD1503_CS_PIN                       (P22_2)                             // CS 引脚定义
#define PD1503_CSN(x)                       ((x) ? (gpio_high(PD1503_CS_PIN)) : (gpio_low(PD1503_CS_PIN)))

//=================================================PD1503 常量定义==================================================
#define PD1503_CMD_STREAM                   (0x05)                              // STREAM 读取命令字
#define PD1503_MAX_RETRY                    (6)                                 // CRC 校验失败最大重试次数

#define PD1503_CRC8_POLY                    (0x1D)                              // CRC8 SAE J1850 多项式
#define PD1503_CRC8_INIT                    (0xFF)                              // CRC8 初始值
#define PD1503_CRC8_XOR_OUT                 (0xFF)                              // CRC8 异或输出

#define PD1503_RESOLUTION                   (65536.0f)                          // 16bit 分辨率

#define PD1503_STATUS_PARITY_BIT            (0x01)                              // bit[0]: 奇偶校验位
#define PD1503_STATUS_MAG_ERR_BIT           (0x80)                              // bit[7]: 磁场异常报警

//=================================================PD1503 数据结构==================================================

// 编码器解码结果
typedef struct
{
    uint16  angle_count;                // 16bit 角度原始计数值
    float   angle_deg;                  // 换算后的角度 (°), 0~359.99
    uint8   status;                     // 状态字节
    uint8   crc;                        // 接收到的 CRC8 值
    uint8   mag_error;                  // 1 = 磁场异常
    uint8   parity_ok;                  // 1 = 奇偶校验通过
    uint8   crc_ok;                     // 1 = CRC 校验通过
} pd1503_data_struct;

//=================================================PD1503 外部函数==================================================
uint8   pd1503_crc8_sae_j1850      (const uint8 *data, uint8 length);       // CRC8 SAE J1850 校验计算
uint8   pd1503_verify_parity       (uint16 angle_raw, uint8 status);        // 奇偶校验
uint8   pd1503_read_angle          (pd1503_data_struct *pd1503_data);        // 读取角度 (完整数据+校验)
uint16  pd1503_get_count           (void);                                  // 快速获取原始计数值
float   pd1503_get_angle_deg       (void);                                  // 快速获取角度值 (°)
uint8   pd1503_init                (void);                                  // 初始化
//=================================================PD1503 外部函数==================================================

#endif